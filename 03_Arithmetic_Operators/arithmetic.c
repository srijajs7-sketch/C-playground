#include <stdio.h>

int main() {

    int a = 10;
    int b = 3;

    // Addition
    printf("Addition: %d\n", a + b);

    // Subtraction
    printf("Subtraction: %d\n", a - b);

    // Multiplication
    printf("Multiplication: %d\n", a * b);

    // Division
    printf("Division: %d\n", a / b);

    // Modulus (remainder)
    printf("Remainder: %d\n", a % b);

    return 0;
}